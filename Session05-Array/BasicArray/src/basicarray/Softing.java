package basicarray;


public class Softing {
    public static void main(String[] args) {
        playWithPrimitiveArrayV4();
    }
    
    public static void playWithPrimitiveArrayV4() {
        int[] a = new int[]{5, 10, 15, 20, 25, 30};
        for (int x: a) {
            System.out.println(x);
        }
    }
    
}
